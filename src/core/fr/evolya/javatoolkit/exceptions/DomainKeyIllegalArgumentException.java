package fr.evolya.javatoolkit.exceptions;

@SuppressWarnings("serial")
public class DomainKeyIllegalArgumentException extends RuntimeException {

	public DomainKeyIllegalArgumentException() {
		super();
	}

	public DomainKeyIllegalArgumentException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DomainKeyIllegalArgumentException(String arg0) {
		super(arg0);
	}

	public DomainKeyIllegalArgumentException(Throwable arg0) {
		super(arg0);
	}

}
